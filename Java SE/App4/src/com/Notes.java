package com;

public class Notes {
	
	/*
	 * 	Methods
	 * ------------
	 *  1. Method without return type and with out arguments
	 *  2. Method with return type and with out arguments
	 *  3. Method with out return type and with arguments
	 *  4. Method with return type and with arguments
	 * 
	 * Note: 
	 * 	-> Every method must return a value
	 * 	-> if a method is not returning anything, then
	 * 	   return type must be 'void'
	 *  -> Every method can return only one value.
	 *  
	 *  ----------------------------------------------
	 *  
	 *  
	 *  Method defination/called method
	 *  ---------------------------------
	 *  -> method with body or business logic 
	 *  
	 *  method Declaration / calling method
	 *  -------------------------------------
	 *  -> method who invokes method defination
	 *  
	 *  
	 *  Types of Arguments
	 *  -------------------
	 *  	-> Actual Argument
	 *  	-> Formal Argument
	 *  
	 *  
	 *  
	 * Call By value:
	 * ---------------
	 * Any changes to the formal argument will not impact on actual argument
	 * Note: all primitive types in java are call by value
	 * 
	 * 
	 * Call By referance
	 * ------------------
	 * 
	 * Any changes to the formal argument will impact on actual argument
	 * 
	 * Note: Objects are always call by referance
	 * 
	 * 
	 * 
	 * 
	 *  
	 *  
	 */

}
