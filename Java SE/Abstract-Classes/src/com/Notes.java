/*
 * Notes
 * -----------
 * 	-> Abstract class is a method specification which contains zero or more
 *     abstract methods and concrete methods
 *  -> An abstract method do not specify a body
 *  -> An abstract method must be defined only in the abstract class
 *  -> We can not create an object to an  abstract class. But we can 
 *     create a reference variable to the abstract class
 *  -> All the sub classes of abstract class must override all the 
 *     abstract methods of the abstract class.
 *  -> If any sub class of an abstract class not overriding any one of the
 *     abstract method, then that sub class must be defined as 'abstract;
 * 
 */

